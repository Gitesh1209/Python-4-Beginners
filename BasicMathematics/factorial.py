def factorial(n):
    try:
        assert n>=0
        if n != 1 and n !=0 :
            return n*factorial(n-1)
        elif n==1 or n==0:
            return 1
    except:
        print('Negative numbers dont have factorial')
    
        
num = int(input('Enter the number = '))
res = factorial(num)
print('Factorial of',num,'is',res)
  
